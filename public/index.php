					Roadmap
				</h3>

				<h4>Stage One: Completed</h4>
				<h5>Initial outreach, ensure that Catalyst would be useful and that there is a demand.</h5>

				<p class="flow-text">This stage focused on the conceptualization of the project, reaching out to artists and buyers alike to see what they would want in a service such as this.  We found that there is a high demand for Catalyst, and have developed a skeleton of features for the site.</p>

				<h4>Stage Two: In progress</h4>
				<h5>Development of infrastructure and basic creation of the brand.</h5>

				<p class="flow-text">In this stage, we are developing the main components of the service.  Additionally, we are beginning to advertise and build support around the brand through community outreach, posting to social media and talking directly with fandom members.</p>

				<h4>Stage Three</h4>
				<h5>Big time advertisement, initial users.</h5>

				<p class="flow-text">During this stage, we will begin to focus on recruiting members, potentially through advertising.  We may provide incentives for artists to join and spread the word about the service.</p>

				<h4>Stage Four</h4>
				<h5>Initial user feedback and additional features.</h5>

				<p class="flow-text">Once we have a sufficient user base, we will begin to act on the feedback users provide and implement non-core features.  In this stage we will clean the interface up, polishing it.</p>

				<h4>Stage Five</h4>
				<h5>Optimization of the server and its code.</h5>

				<p class="flow-text">At this point we will be expanding exponentially, and, as such, will have increased server load.  In order to provide the best service possible to our users, we will devote this stage to optimization the site, even if it decreases load time by only a few milliseconds.</p>

				<h4>Stage Six</h4>
				<h5>???</h5>

				<p class="flow-text">By this time the service will have all of its features implemented.  In this time we will work on policy issues and bugs which may not have been seen.  Additionally, we will continue to get the feedback of our user base, potentially adding more features.</p>

				<h4>Stage Seven</h4>
				<h5>Profit</h5>

				<p class="flow-text">After the site is complete we will try to find a way to drive profit for it, either through devices such as promoted artists or donation-based services such as Patreon.</p>
